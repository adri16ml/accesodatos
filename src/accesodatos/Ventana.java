    public String abrirArchivo(File archivo){
      // guardamos el documento
        String documento= "";
        try{
            //try catch para evitar errores.
          entrada = new FileInputStream(archivo);
          // almacenamos el codigo ascii del documento 
          int asc;
          // pasamos el documento en ascii a un texto 
          while((asc=entrada.read())!=-1){
              // le damos a cada character su valor correspondiente 
              char c = (char)asc;
              documento+=c;
              
          }
        }catch(Exception e){
            
        }
        // ahora devuelve el texto ya escrito 
        return documento;
    }
    public String GuardarArchivo(File archivo,String Documento){
        String mensaje = null;
        try{
            salida = new FileOutputStream(archivo);
            byte[] bytxt=Documento.getBytes();
            salida.write(bytxt);
            mensaje="archivo guardado";
        }catch(Exception e){            
        }
        return mensaje;
    }

