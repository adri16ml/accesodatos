    private void elegirArchivoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_elegirArchivoMouseClicked
        // elegimos el archivo 
        // creamos un jfilechooser para seleccionar el archivo que va a abrir el usuario.
             if(seleccionar.showDialog(null, "abrir")==JFileChooser.APPROVE_OPTION){
           archivo=seleccionar.getSelectedFile();
           // si se puede abrir el archivo
           if(archivo.canRead()){
               // le pasa el nombre siempre y cuando sea un .txt es decir un texto plano
               if(archivo.getName().endsWith("txt")){
                   String  documento = abrirArchivo(archivo);
                   // lo imprime en la pantalla inicial
                   textoArea.setText(documento);
                   // lo imprime en la pantalla secundaria del texto secuencial              
                   txtSecuencial.setText(documento);
               }else{
                   JOptionPane.showMessageDialog(null, "No es compatible");
               }
           }
       }
    }//GEN-LAST:event_elegirArchivoMouseClicked

